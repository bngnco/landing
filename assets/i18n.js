/* ------------------------------------------------------------
   Translator: replaces star-wrapped tokens with cream em spans,
   so any language can mark its emphasis with ★...★.
   ------------------------------------------------------------ */

window.VERIFYCO_TR = function(code, key) {
  const dict = window.VERIFYCO_I18N[code] || window.VERIFYCO_I18N.en;
  let str = dict[key];
  if (str == null) str = window.VERIFYCO_I18N.en[key] || key;
  // Replace ★...★ with a gradient highlight span (NOT italic — italic is
  // reserved for the brand wordmark only).
  return str.replace(/★(.+?)★/g, '<span class="hl">$1</span>');
};
